<script>
import HomeDarkNavigation from "../components/HomeDarkNavigation.vue";
import HomeFooter from "../components/HomeFooter.vue";
import profit from "../components/profit.vue";
import Testimonial from "../components/Testimonial.vue";
export default {
  data() {
    return {
      programs: [],
      address: "",
      email: "",
      phone: "",
      blogs: [],
    };
  },
  methods: {
    getPrograms(data) {
      const programs = [];
      data.forEach((el) => {
        if (el.category == "Programs") {
          programs.push(el);
        }
      });
      return programs;
    },

    getServices(data) {
      const programs = [];
      data.forEach((el) => {
        if (el.category == "Service") {
          programs.push(el);
        }
      });
      return programs;
    },

    formatDate(data) {
      const date = new Date(data).getTime(); // Note that month is zero-indexed in JavaScript
      const options = {
        year: "numeric",
        month: "long",
        day: "numeric",
        suffix: (day) => {
          if (day === 1 || day === 21 || day === 31) {
            return "st";
          } else if (day === 2 || day === 22) {
            return "nd";
          } else if (day === 3 || day === 23) {
            return "rd";
          } else {
            return "th";
          }
        },
      };

      return new Intl.DateTimeFormat("en-US", options).format(date);
    },

    loadScript() {
      if (!process.server) {
        let el = document.getElementById("script");

        if (el != undefined) {
          document.body.removeChild(el);
        }

        const script = document.createElement("script");
        script.type = "text/javascript";
        script.src = "/script/home.js";
        script.async = true;
        script.id = "script";
        const app = document.querySelector("#footer");
        if (app) {
          app.appendChild(script);
        } else {
          console.error("Could not find app node to append script element");
        }
      }
    },

    async getCompany() {
      try {
        const result = await this.$axios.get("/company");
        this.company = await result.data.data;
        this.address = await result.data.data.media[0];
        this.email = await result.data.data.media[1];
        this.phone = await result.data.data.media[2];
      } catch (err) {
        console.log(err.response.data);
      }
    },

    async getBlogs() {
      try {
        const result = await this.$axios.get("/blog");
        this.programs = this.getPrograms(result.data.data);
        this.blogs = this.getServices(result.data.data);
      } catch (err) {
        console.log(err.response);
      }
    },
  },
  mounted() {
    this.getCompany();
    this.getBlogs();
  },
  computed: {
    FILE_URL() {
      return this.$store.state.fileURL;
    },
  },
  components: { profit, Testimonial, HomeFooter, HomeDarkNavigation },
};
</script>

